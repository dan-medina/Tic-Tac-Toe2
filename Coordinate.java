
public class Coordinate
{
    private int row;
    private int col;

    /**
     * Constructor for objects of class Coordinate
     */
    public Coordinate(int row, int col)
    {
        this.row = row;
        this.col = col;
    }
    public int getRow() {
        return row;
    }
    public int getCol() {
        return col;
    }
}
